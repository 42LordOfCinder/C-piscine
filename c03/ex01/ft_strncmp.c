int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	n1;
	int				i;

	i = 0;
	n1 = 0;
	if (n == 0)
		return (0);
	while (s1[i] != '\0' && s2[i] != '\0' && s1[i] == s2[i] && n1 < n - 1)
	{
		i++;
		n1++;
	}
	return (s1[i] - s2[i]);
}
