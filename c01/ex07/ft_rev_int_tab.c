void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	j;
	int	k;

	i = 0;
	while (i < size / 2)
	{
		j = tab[i];
		k = tab[size - i - 1];
		tab[i] = k;
		tab[size - i - 1] = j;
		i++;
	}
}
