#include "ft.h"

int	ft_sum(int n1, int n2)
{
	return (n1 + n2);
}

int	ft_sub(int n1, int n2)
{
	return (n1 - n2);
}

int	ft_mult(int n1, int n2)
{
	return (n1 * n2);
}

int	ft_div(int n1, int n2)
{
	return (n1 / n2);
}

int	ft_mod(int n1, int n2)
{
	return (n1 % n2);
}
