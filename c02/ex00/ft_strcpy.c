char	*ft_strcpy(char *dest, char *src)
{
	int	i;

	i = 0;
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

#include <stdio.h>
int	main(void)
{
	char	dest[100] = "";

	printf("%s", ft_strcpy(dest, "eAky9Nq1Pawoi9oxmRliyaFL"));
	return (0);
}
